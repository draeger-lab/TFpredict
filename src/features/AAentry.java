package features;

public class AAentry {
	
	String Amino;
	double Hpho;
	double Hphi;
	int Mass;
	
	
	public AAentry () {
	}
	
	public AAentry(String amino, double hpho, double hphi, int mass) {
		this.Amino = amino;
		this.Hpho = hpho;
		this.Hphi = hphi;
		this.Mass = mass;
		
	}

}
